    public void refreshView(){
        ArrayList<Directory> dirs = this.fs.getCurrent().getDirectories();
        this.list1.removeAll();
        for(Directory dir : dirs){
            System.out.println(dir.getName());
            this.list1.add(dir.getName());
        }
    }

